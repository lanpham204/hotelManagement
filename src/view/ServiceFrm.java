    private List<Service> serviceList;
    private List<TypeOfService> typeList;

    private void init() {
        modelService = (DefaultTableModel) tblService.getModel();
        modelType = (DefaultTableModel) tblType.getModel();
        fillTblType();
        fillTblService();
    }

    private void fillTblService() {
        serviceList = serviceDAO.selectAll();
        modelService.setRowCount(0);
        typeList = typeDAO.selectAll();
        for (Service x : serviceList) {
            for (TypeOfService y : typeList) {
                if (y.getId().equals(x.getIdTypeOfService())) {
                    System.out.println("s");
                    modelService.addRow(new Object[]{
                        x.getId(), x.getName(), y.getName(), numberFormat.format(x.getPrice())
                    });
                }
            }
        }
        modelService.fireTableDataChanged();
        cleanService();
    }

    private void fillTblType() {
        typeList = typeDAO.selectAll();
        modelType.setRowCount(0);
        for (TypeOfService x : typeList) {
            modelType.addRow(new Object[]{
                x.getId(), x.getName()
            });
        }
        modelType.fireTableDataChanged();
        fillCboType();
        cleanType();
    }

    private void fillCboType() {
        cboType.removeAllItems();
        for (TypeOfService x : typeList) {
            cboType.addItem(x.getName());
        }
    }

    private TypeOfService checkType() {
        String Id = txtType.getText();
        String name = txtNameType.getText();
        if (Id.isEmpty() || name.isEmpty()) {
            MsgBox.showMessage(this, "Vui lòng nhập đầy đủ thông tin");
            return null;
        }
        return new TypeOfService(Id, name);
    }

    private Service checkService() {
        String name = txtNameService.getText();
        String price = txtPrice.getText();
        if (name.isEmpty() || price.isEmpty()) {
            MsgBox.showMessage(this, "Vui lòng nhập đầy đủ thông tin");
            return null;
        }
        float pric = 0;
        try {
            pric = Float.parseFloat(price);
        } catch (Exception e) {
            MsgBox.showMessage(this, "Đơn giá phải là số ");
            return null;
        }

        return new Service(name, pric, typeList.get(cboType.getSelectedIndex()).getId());
    }

    private void cleanService() {
        txtPrice.setText("");
        txtNameService.setText("");
    }

    private void cleanType() {
        txtType.setText("");
        txtNameType.setText("");
    }
